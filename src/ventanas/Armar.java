    public String ConseguirLugar (){
        String lugar="";
        if(bosque.isSelected()){
            lugar="bosque";
        }
        if(casa.isSelected()){
            lugar="casa";
        }
        if(castillo.isSelected()){
            lugar="castillo";
        }
        if(ciudad.isSelected()){
            lugar="ciudad";
        }
        return lugar;
    }
