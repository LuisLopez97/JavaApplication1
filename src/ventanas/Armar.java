    private void ComenzarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ComenzarActionPerformed
        // TODO add your handling code here:
        if(niño.isSelected()&niña.isSelected()&hombre.isSelected()&mujer.isSelected()){
        JOptionPane.showMessageDialog(null,"Por favor, selecciona de 0 a 3 personajes.");
         }
      else{
          if(niño.isSelected()){
              if(!personaje1.equals("")){
                  if(!personaje2.equals("")){
                      if(personaje3.equals("")){
                          personaje3="nino";
                      }
                  }
                  else{
                          personaje2="nino";
                      }
              }
              else{
                  personaje1="nino";
              }
          }
          if(niña.isSelected()){
              if(!personaje1.equals("")){
                  if(!personaje2.equals("")){
                      if(personaje3.equals("")){
                          personaje3="nina";
                      }
                  }
                      else{
                          personaje2="nina";
                      }
              }
              else{
                  personaje1="nina";
              }
          }
          if(hombre.isSelected()){
              if(!personaje1.equals("")){
                  if(!personaje2.equals("")){
                      if(personaje3.equals("")){
                          personaje3="hombre";
                      }
                  }
                      else{
                          personaje2="hombre";
                      }
              }
              else{
                  personaje1="hombre";
              }
          }
          if(mujer.isSelected()){
              if(!personaje1.equals("")){
                  if(!personaje2.equals("")){
                      if(personaje3.equals("")){
                          personaje3="mujer";
                      }
                  }
                      else{
                          personaje2="mujer";
                      }
              }
              else{
                  personaje1="mujer";
              }
          }
          if(!personaje1.equals("")&personaje2.equals("")&personaje3.equals("")){
              personajes=1;
          }
          if(!personaje1.equals("")&!personaje2.equals("")&personaje3.equals("")){
              personajes=2;
          }
          if(!personaje1.equals("")&!personaje2.equals("")&!personaje3.equals("")){
              personajes=3;
          }
          des=Finales.getSelectedItem().toString();
          Cuento cuento = new Cuento(personaje1,personaje2,personaje3,lugar,des,personajes);
          cuento.setVisible(true);
          this.dispose();
      }
    }//GEN-LAST:event_ComenzarActionPerformed

